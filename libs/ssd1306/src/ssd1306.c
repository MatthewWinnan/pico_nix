// Framebuffer: 4 pages × 128 columns = 512 bytes.
// fb[page * 128 + col] — each byte is 8 vertical pixels (bit0 = top).
static uint8_t fb[SSD1306_FB_SIZE];

// Reuse one static transfer buffer to avoid stack pressure on ssd1306_show.
static uint8_t xfer[SSD1306_FB_SIZE + 1];

