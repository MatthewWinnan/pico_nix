static int char_to_idx(char c) {
    if (c >= 'A' && c <= 'Z') return 1 + (c - 'A');
    if (c >= 'a' && c <= 'z') return 1 + (c - 'a');
    if (c >= '0' && c <= '9') return 27 + (c - '0');
    switch (c) {
        case '.': return 37;
        case ':': return 38;
        case '/': return 39;
        case '>': return 40;
        default:  return 0;
    }
}

static void write_cmd(uint8_t cmd) {
    uint8_t buf[2] = {0x80, cmd};  // 0x80 = Co=1, D/C#=0 (single command)
    i2c_write_blocking(i2c0, SSD1306_I2C_ADDR, buf, 2, false);
}

void ssd1306_init(void) {
    i2c_init(i2c0, 400 * 1000);
    gpio_set_function(SSD1306_I2C_SDA_PIN, GPIO_FUNC_I2C);
    gpio_set_function(SSD1306_I2C_SCL_PIN, GPIO_FUNC_I2C);
    gpio_pull_up(SSD1306_I2C_SDA_PIN);
    gpio_pull_up(SSD1306_I2C_SCL_PIN);

    sleep_ms(100);  // allow display power-on

    write_cmd(SSD1306_SET_DISP);                  // display off during init
    write_cmd(SSD1306_SET_MEM_MODE);   write_cmd(0x00);  // horizontal addressing
    write_cmd(SSD1306_START_LINE);                // display start line = 0
    write_cmd(SSD1306_SET_SEG_REMAP | 0x01);      // col 127 → SEG0
    write_cmd(SSD1306_SET_MUX_RATIO); write_cmd(SSD1306_HEIGHT - 1);  // 31
    write_cmd(SSD1306_SET_COM_OUT_DIR | 0x08);    // scan COM[N-1]→COM0
    write_cmd(SSD1306_SET_DISP_OFFSET); write_cmd(0x00);
    write_cmd(SSD1306_SET_COM_PIN_CFG); write_cmd(0x02);  // 128×32 specific
    write_cmd(SSD1306_SET_CLK_DIV);    write_cmd(0x80);
    write_cmd(SSD1306_SET_PRECHARGE);  write_cmd(0xF1);
    write_cmd(SSD1306_SET_VCOM_DESEL); write_cmd(0x30);
    write_cmd(SSD1306_SET_CONTRAST);   write_cmd(0xFF);
    write_cmd(SSD1306_SET_ENTIRE_ON);             // follow RAM content
    write_cmd(SSD1306_SET_NORM_DISP);             // not inverted
    write_cmd(SSD1306_SET_CHARGE_PUMP); write_cmd(0x14);
    write_cmd(SSD1306_SET_SCROLL_OFF);
    write_cmd(SSD1306_SET_DISP | 0x01);           // display on

    ssd1306_clear();
    ssd1306_show();
}

void ssd1306_clear(void) {
    memset(fb, 0, SSD1306_FB_SIZE);
}

void ssd1306_show(void) {
    write_cmd(SSD1306_SET_COL_ADDR);  write_cmd(0); write_cmd(SSD1306_WIDTH - 1);
    write_cmd(SSD1306_SET_PAGE_ADDR); write_cmd(0); write_cmd(SSD1306_PAGES - 1);

    xfer[0] = 0x40;  // Co=0, D/C#=1 — all subsequent bytes are data
    memcpy(xfer + 1, fb, SSD1306_FB_SIZE);
    i2c_write_blocking(i2c0, SSD1306_I2C_ADDR, xfer, SSD1306_FB_SIZE + 1, false);
}

void ssd1306_puts(int col, int row, const char *s) {
    while (*s && col < 16) {
        int idx = char_to_idx(*s++);
        for (int fc = 0; fc < 8; fc++) {
            int pixel_col = col * 8 + fc;
            if (pixel_col >= SSD1306_WIDTH) break;
            fb[row * SSD1306_WIDTH + pixel_col] = font[idx][fc];
        }
        col++;
    }
}
